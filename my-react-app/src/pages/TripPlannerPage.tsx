// ─── Types ────────────────────────────────────────────────────────────────────

type PageView = 'setup' | 'generating' | 'result';

interface GeneratedPlan {
  destination: string;
  origin: string;
  startDate: string;
  endDate: string;
  numDays: number;
  destInfo: DestinationInfo;
  days: DayTemplate[];
  flight: FlightOffer | null;
  hotel: HotelOffer | null;
  preferences: string[];
  aiNarrative: string | null;
  totalBudgetMYR: number;
}

// ─── Helpers ──────────────────────────────────────────────────────────────────

const DESTINATIONS_LIST = ['London', 'Paris', 'Tokyo', 'Sydney', 'Melbourne', 'Bali', 'Bangkok', 'Singapore', 'Seoul', 'Dubai'];

function daysBetween(start: string, end: string): number {
  if (!start || !end) return 5;
  const s = new Date(start);
  const e = new Date(end);
  const diff = Math.round((e.getTime() - s.getTime()) / (1000 * 60 * 60 * 24));
  return Math.max(1, diff);
}

function formatDate(dateStr: string): string {
  if (!dateStr) return '';
  const d = new Date(dateStr);
  return d.toLocaleDateString('en-GB', { day: 'numeric', month: 'short', year: 'numeric' });
}

function addDays(dateStr: string, days: number): string {
  if (!dateStr) return '';
  const d = new Date(dateStr);
  d.setDate(d.getDate() + days);
  return d.toLocaleDateString('en-GB', { weekday: 'short', day: 'numeric', month: 'short' });
}

function getAccomLevel(prefs: string[]): 'budget' | 'comfort' | 'luxury' {
  if (prefs.includes('Luxury'))  return 'luxury';
  if (prefs.includes('Premium')) return 'comfort';
  return 'budget';
}

// ─── PDF Generator ────────────────────────────────────────────────────────────

function exportToPDF(plan: GeneratedPlan) {
  const doc = new jsPDF({ orientation: 'portrait', unit: 'mm', format: 'a4' });
  const W = 210;
  const margin = 15;
  let y = margin;

  const purple = [123, 44, 191] as [number, number, number];
  const blue   = [68, 97, 242]  as [number, number, number];

  const checkY = (needed: number) => {
    if (y + needed > 280) { doc.addPage(); y = margin; }
  };

  doc.setFillColor(...purple);
  doc.rect(0, 0, W, 45, 'F');
  doc.setTextColor(255, 255, 255);
  doc.setFontSize(22);
  doc.setFont('helvetica', 'bold');
  doc.text(`${plan.destInfo.emoji}  ${plan.destination} Trip Itinerary`, margin, 20);
  doc.setFontSize(11);
  doc.setFont('helvetica', 'normal');
  doc.text(`${plan.origin}  →  ${plan.destination}  ·  ${formatDate(plan.startDate)} – ${formatDate(plan.endDate)}  ·  ${plan.numDays} Days`, margin, 32);
  doc.text(`Generated by ZenTravel AI  ·  ${new Date().toLocaleDateString()}`, margin, 40);
  y = 55;

  doc.setTextColor(0, 0, 0);
  doc.setFontSize(13);
  doc.setFont('helvetica', 'bold');
  doc.text('TRAVEL ESSENTIALS', margin, y);
  y += 7;
  doc.setDrawColor(...purple);
  doc.line(margin, y, W - margin, y);
  y += 7;

  doc.setFontSize(10);
  doc.setFont('helvetica', 'normal');
  if (plan.flight) {
    doc.setFont('helvetica', 'bold');
    doc.text('✈  Recommended Flight', margin, y);
    doc.setFont('helvetica', 'normal');
    doc.text(`${plan.flight.airline}  ${plan.flight.flightNumber}  ·  ${plan.flight.cabin}  ·  MYR ${plan.flight.priceMYR.toLocaleString()}`, margin + 5, y + 6);
    y += 15;
  }
  if (plan.hotel) {
    doc.setFont('helvetica', 'bold');
    doc.text('🏨  Recommended Hotel', margin, y);
    doc.setFont('helvetica', 'normal');
    doc.text(`${plan.hotel.name}  ·  ${'★'.repeat(plan.hotel.stars)}  ·  MYR ${plan.hotel.priceMYR}/night  ·  ${plan.hotel.distance}`, margin + 5, y + 6);
    y += 15;
  }

  const budgetLevel = getAccomLevel(plan.preferences);
  const daily = plan.destInfo.avgDailyBudgetMYR[budgetLevel];
  doc.setFont('helvetica', 'bold');
  doc.text('💰  Estimated Budget', margin, y);
  doc.setFont('helvetica', 'normal');
  doc.text(`~MYR ${(daily * plan.numDays + (plan.flight?.priceMYR ?? 0)).toLocaleString()} total  (MYR ${daily}/day × ${plan.numDays} days + flights)`, margin + 5, y + 6);
  y += 18;

  for (const day of plan.days) {
    checkY(55);
    doc.setFillColor(...blue);
    doc.rect(margin, y, W - margin * 2, 10, 'F');
    doc.setTextColor(255, 255, 255);
    doc.setFontSize(11);
    doc.setFont('helvetica', 'bold');
    doc.text(`  DAY ${day.dayNumber}  –  ${day.theme.toUpperCase()}  (${addDays(plan.startDate, day.dayNumber - 1)})`, margin + 2, y + 7);
    y += 14;
    doc.setTextColor(0, 0, 0);
    doc.setFontSize(9);
    doc.setFont('helvetica', 'normal');

    const activities = [
      day.morningActivity   && { time: 'Morning',   act: day.morningActivity },
      day.afternoonActivity && { time: 'Afternoon', act: day.afternoonActivity },
      day.eveningActivity   && { time: 'Evening',   act: day.eveningActivity },
    ].filter(Boolean) as { time: string; act: NonNullable<DayTemplate['morningActivity']> }[];

    for (const { time, act } of activities) {
      checkY(18);
      doc.setFont('helvetica', 'bold');
      doc.text(`${time}: ${act.name}`, margin + 3, y);
      doc.setFont('helvetica', 'normal');
      const lines = doc.splitTextToSize(act.description, W - margin * 2 - 8);
      doc.text(lines, margin + 5, y + 5);
      if (act.entryFee) doc.text(`Entry: ${act.entryFee}`, margin + 5, y + 5 + lines.length * 4 + 1);
      y += 5 + lines.length * 4 + (act.entryFee ? 5 : 2) + 2;
    }

    checkY(20);
    doc.setFont('helvetica', 'bold');
    doc.text('🍽  Must-Try Today', margin + 3, y);
    y += 5;
    doc.setFont('helvetica', 'normal');
    const meals = [
      `Breakfast: ${day.meals.breakfast.name} @ ${day.meals.breakfast.where}`,
      `Lunch: ${day.meals.lunch.name} @ ${day.meals.lunch.where}`,
      `Dinner: ${day.meals.dinner.name} @ ${day.meals.dinner.where}`,
    ];
    for (const m of meals) {
      checkY(6);
      doc.text(`  • ${m}`, margin + 3, y);
      y += 5;
    }

    checkY(10);
    doc.setTextColor(...purple);
    doc.setFont('helvetica', 'italic');
    doc.text(`💡 Tip: ${day.tip}`, margin + 3, y + 2);
    doc.setTextColor(0, 0, 0);
    doc.setFont('helvetica', 'normal');
    y += 10;
  }

  doc.addPage();
  doc.setFillColor(...purple);
  doc.rect(0, 0, W, 30, 'F');
  doc.setTextColor(255, 255, 255);
  doc.setFontSize(14);
  doc.setFont('helvetica', 'bold');
  doc.text('General Travel Tips', margin, 18);
  y = 40;
  doc.setTextColor(0, 0, 0);
  doc.setFontSize(10);
  doc.setFont('helvetica', 'normal');
  for (const tip of plan.destInfo.tips) {
    checkY(10);
    const lines = doc.splitTextToSize(`• ${tip}`, W - margin * 2);
    doc.text(lines, margin, y);
    y += lines.length * 5 + 3;
  }

  doc.save(`ZenTravel_${plan.destination}_${plan.startDate || 'Trip'}.pdf`);
}

